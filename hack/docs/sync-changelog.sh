#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
cd "${ROOT_DIR}"

if [ "$#" -ne 2 ]; then
  echo "Usage: $0 <source changelog> <destination mdx>" >&2
  exit 1
fi

SOURCE="$1"
DESTINATION="$2"

{
  echo "{/* AUTOGENERATED — do not edit. Run \`task sync-changelog\` to regenerate from ${SOURCE} */}"
  echo
  awk '
    /^## \[Unreleased\]/ { release=0; next }
    /^## \[[0-9]+\.[0-9]+\.[0-9]+\]/ {
      release=1
      sub(/\[/, "v"); sub(/\]/, "")
    }
    release
  ' "${SOURCE}"
} > "${DESTINATION}"
