#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
cd "${ROOT_DIR}"

sync_changelog() {
  local key="$1"
  local source="$2"
  local destination="frontend/docs/pages/reference/changelog/${key}.mdx"

  {
    echo "{/* AUTOGENERATED — do not edit. Run \`task sync-changelog\` to regenerate from ${source} */}"
    echo
    awk '
      /^## \[Unreleased\]/ { release=0; next }
      /^## \[[0-9]+\.[0-9]+\.[0-9]+\]/ {
        release=1
        sub(/\[/, "v"); sub(/\]/, "")
      }
      release
    ' "${source}" | awk '
      NF {
        line=$0
        if (line !~ /^ /) {
          gsub(/  +/, " ", line)
        }
        if (previous_heading && !blank) {
          print ""
        }
        blank=0
        print line
        previous_heading=(line ~ /^#+ /)
        next
      }
      !blank { print; blank=1 }
      { previous_heading=0 }
    '
  } > "${destination}"
}

sync_changelog "platform" "CHANGELOG.md"
sync_changelog "python" "sdks/python/CHANGELOG.md"
sync_changelog "typescript" "sdks/typescript/CHANGELOG.md"
sync_changelog "ruby" "sdks/ruby/src/CHANGELOG.md"
